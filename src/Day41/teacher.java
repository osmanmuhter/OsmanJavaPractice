package Day41;

public class teacher {

	private String name;
	private String major;
	private long ssn;
	
	public String getName() {
		return name;
	}
	
	public String getMajor() {
		return major;
	}
	
	public long getSSN() {
		return ssn;
	}
	
	public void setName(String nNames) {
		name = nNames;
	}
	
	public void setMajor(String nMajor) {
		major = nMajor;
		
	}
	
	public void setSSN(long nnums) {
		ssn = nnums;
	}
}
